package Modelo;

import java.io.Serializable;

/**
 *
 * @author Paulo Aguilar
 */
public class Coordenada implements Serializable {
    
    private double x;
    private double y;
    
    public Coordenada(double x, double y){
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
